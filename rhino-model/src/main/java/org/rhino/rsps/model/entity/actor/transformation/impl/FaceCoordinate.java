package org.rhino.rsps.model.entity.actor.transformation.impl;

public class FaceCoordinate {

    private final int x;
    private final int y;

    public FaceCoordinate(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
